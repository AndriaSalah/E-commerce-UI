export function renderStars(Rating, Color="var(--textColor)"){
    let stars=[]
    for (let i = 1; i<=5 ; i++,Rating--){
        if(Rating>0) stars.push(<AiFillStar key={i} style={{color:Color}}/>)
        else stars.push(<AiOutlineStar key={i}/>)
    }
    return(
        stars
    )
}


